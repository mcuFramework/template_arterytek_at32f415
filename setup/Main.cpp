/* ****************************************************************************************
 * Using
 */
 
//-----------------------------------------------------------------------------------------
using namespace core;

//-----------------------------------------------------------------------------------------
using mcuf::Thread;

/* ****************************************************************************************
 * Extern
 */

/* ****************************************************************************************
 * Class
 */

/* ****************************************************************************************
 * Variable
 */
mcuf::hal::GeneralPin* led[8];

/* ****************************************************************************************
 * Method
 */

void setup(Thread* _this){
  for(int i=0; i<8; ++i){
    led[i] = new CoreGeneralPin(Core::gpiob, i);
    led[i]->setOutput();
    led[i]->setLow();
  }
  System::out().print("init\n");
  _this->delay(100);
}

void loop(Thread* _this){
  for(int i=0; i<8; ++i){
    led[i]->setToggle();
    _this->delay(100);
  }
}
 
